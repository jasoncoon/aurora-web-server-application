package aurora;

public class AudioPatternWrapper {
    private String audioPattern;

    public AudioPatternWrapper() {
    }

    public AudioPatternWrapper(String audioPattern) {
        this.audioPattern = audioPattern;
    }

    public String getAudioPattern() {
        return audioPattern;
    }

    public void setAudioPattern(String audioPattern) {
        this.audioPattern = audioPattern;
    }
}
